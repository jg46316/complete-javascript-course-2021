// **************** NOTES: DATA TYPES *******************

// In Javascript every value is either an object or a primitive value

/* Primitive data types
1- Number: Floating point numbers - Used for decimal integers let age = 23
2- String: Sequence of characters - Used for text
3- Boolean: Logical type that can be only true or false - Used for taking decisions
4- Undefined: Value taken by a variable that is not yet defined (empty value)
5- Null: Also means empty value
6- Symbol: Value that is unique and can not be changed
7- BigInt: Larger integers than the number type can hold */

// Javascript has dynamic typing: We do not have to manually define the data of the value stored in a variable. Instead, data types are determined automatically.
